const translateWithDict = (text) => {
  const lower = text.toLowerCase().trim();
  return DICT.get(lower) || null;
};

const translateWithMyMemory = async (text) => {
  try {
    const { data } = await axios.get(
      'https://api.mymemory.translated.net/get',
      { params: { q: text, langpair: 'en|es' }, timeout: 3000 }
    );
    return data?.responseData?.translatedText || null;
  } catch {
    return null;
  }
};

const translateWithGoogle = async (text) => {
  const key = process.env.GOOGLE_TRANSLATE_API_KEY;
  if (!key) return null;
  try {
    const { data } = await axios.post(
      `https://translation.googleapis.com/language/translate/v2?key=${key}`,
      { q: text, target: 'es', source: 'en' },
      { timeout: 5000 }
    );
    return data?.data?.translations?.[0]?.translatedText || null;
  } catch {
    return null;
  }
};

const cleanStep = (text) => text.replace(/^Step:\d+\s*/i, '');

const translateText = async (text) => {
  if (!text || text.trim() === '') return text;
  const trimmed = cleanStep(text.trim());

  const cacheKey = trimmed.toLowerCase();
  const cached = cache.get(cacheKey);
  if (cached && Date.now() - cached.timestamp < CACHE_TTL) {
    return cached.text;
  }

  const dictResult = translateWithDict(trimmed);
  if (dictResult) {
    cache.set(cacheKey, { text: dictResult, timestamp: Date.now() });
    return dictResult;
  }

  const mymemory = await translateWithMyMemory(trimmed);
  if (mymemory && mymemory.toLowerCase() !== trimmed.toLowerCase()) {
    cache.set(cacheKey, { text: mymemory, timestamp: Date.now() });
    return mymemory;
  }

  const google = await translateWithGoogle(trimmed);
  if (google) {
    cache.set(cacheKey, { text: google, timestamp: Date.now() });
    return google;
  }

  cache.set(cacheKey, { text: trimmed, timestamp: Date.now() });
  return trimmed;
};

const translateArray = async (arr) => {
  if (!Array.isArray(arr)) return arr;
  return Promise.all(arr.map(item => translateText(item)));
};

const translateExercise = async (exercise) => {
  if (!exercise) return exercise;

  const [name, bodyParts, equipments, targetMuscles, secondaryMuscles, instructions] = await Promise.all([
    translateText(exercise.name),
    translateArray(exercise.bodyParts),
    translateArray(exercise.equipments),
    translateArray(exercise.targetMuscles),
    translateArray(exercise.secondaryMuscles),
    translateArray(exercise.instructions)
  ]);

  return {
    ...exercise,
    name,
    bodyParts,
    bodyPartsRaw: exercise.bodyParts,
    equipments,
    equipmentsRaw: exercise.equipments,
    targetMuscles,
    targetMusclesRaw: exercise.targetMuscles,
    secondaryMuscles,
    secondaryMusclesRaw: exercise.secondaryMuscles,
    instructions,
    translated: true
  };
};

const translateExercises = async (exercises) => {
  if (!Array.isArray(exercises)) return exercises;
  return Promise.all(exercises.map(ex => translateExercise(ex)));
};

const clearCache = () => cache.clear();

module.exports = { translateText, translateExercise, translateExercises, clearCache };
